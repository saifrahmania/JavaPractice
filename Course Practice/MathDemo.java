public class MathDemo {
    public static void main(String [] args){
        float alpha =3;
        float bravo =13;
        
        System.out.println(alpha + bravo);
        System.out.println(alpha - bravo);
        System.out.println(alpha * bravo);
        System.out.println(alpha / bravo);
        System.out.println(alpha % bravo);

        System.out.println((double)bravo/(double)alpha);//this is called type casting 
    }
    
}
